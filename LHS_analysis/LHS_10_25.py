def pol_to_dict(pol):
    res = {}
    for k, t in type_immune.items():
        mask = (pol[0] == t)
        if np.any(mask):
            res[f'meanPol_{k}'] = float(np.mean(pol[1], where=mask))
            res[f'stdPol_{k}'] = float(np.std(pol[1], where=mask))
        else:
            res[f'meanPol_{k}'] = float('nan')
            res[f'stdPol_{k}'] = float('nan')
    return res

